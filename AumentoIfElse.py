#34 FAÇA UM PROGRAMA QUE PERGUNTE O SALARIO DE UM FUNCIONARIO E CALCULE O VALOR DO AUMENTO
# (PARA SALARIOS SUPERIORES A R$ 1250,00 CALCULE 10% E PARA INFERIORES 15%)

print('Vamos ter uma serie de aumentos na empresa!')
print('Desta forma vamos precisar saber o seu salario para calcular seu aumento.')
salario = float(input('Qual é o seu salario? R$ '))
aumento = (salario * 10) / 100
aumento2 = (salario * 15)/ 100
if salario <= 1250:
    print('O Seu salario atual é de R${:.2f} e com o aumento vai para R${:.2f}'.format(salario, aumento2 + salario))
else:
    print('O seu salario atual é de R${:.2f} e com o aumento vai para R${:.2f}'.format(salario, aumento + salario))
